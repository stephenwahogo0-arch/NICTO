    def is_walkable(self, x, y):
        mx, my = int(x), int(y)
        if 0 <= mx < MAP_W and 0 <= my < MAP_H:
            return MAP[my][mx] in WALKABLE
        return False

    def handle_events(self):
        for event in pygame.event.get():
            if event.type == QUIT: self.running = False
            if event.type == KEYDOWN:
                if event.key == K_ESCAPE: self.running = False

    def update(self, dt):
        if self.game_over or self.victory: return
        keys = pygame.key.get_pressed()
        dx, dy = 0.0, 0.0
        forward = (keys[K_w] or keys[K_UP]) - (keys[K_s] or keys[K_DOWN])
        strafe = (keys[K_d] and not keys[K_a]) - (keys[K_a] and not keys[K_d])
        if forward:
            dx += math.cos(self.pa) * forward * PLAYER_SPEED * dt
            dy += math.sin(self.pa) * forward * PLAYER_SPEED * dt
        if strafe:
            dx += math.cos(self.pa + math.pi/2) * strafe * PLAYER_SPEED * 0.7 * dt
            dy += math.sin(self.pa + math.pi/2) * strafe * PLAYER_SPEED * 0.7 * dt
        if keys[K_LEFT]: self.pa -= 2.5 * dt
        if keys[K_RIGHT]: self.pa += 2.5 * dt
        if keys[K_SPACE] or keys[K_LCTRL]:
            now = pygame.time.get_ticks()
            if now - self.last_shot > self.shoot_cooldown and self.ammo > 0:
                self.ammo -= 1
                self.projectiles.append(Projectile(self.px, self.py, self.pa))
                self.last_shot = now

        mx, my = pygame.mouse.get_rel()
        self.pa += mx * MOUSE_SENS

        if self.is_walkable(self.px + dx, self.py): self.px += dx
        if self.is_walkable(self.px, self.py + dy): self.py += dy

        for p in self.projectiles[:]:
            result = p.update(dt, self.is_walkable)
            if result == "hit_wall":
                self.projectiles.remove(p)
                continue
            for e in self.enemies[:]:
                if math.hypot(p.x - e.x, p.y - e.y) < 0.6:
                    e.hp -= p.damage
                    self.projectiles.remove(p)
                    if e.hp <= 0:
                        self.enemies.remove(e)
                        self.score += 100
                    break

        for e in self.enemies[:]:
            result, dmg = e.update(dt, self.px, self.py, self.is_walkable)
            if result == "attack":
                self.hp -= dmg
                angle = math.atan2(self.py - e.y, self.px - e.x)
                self.knockback_x = math.cos(angle) * 3
                self.knockback_y = math.sin(angle) * 3
            if result == "death":
                self.enemies.remove(e)
                self.score += 100

        if self.knockback_x != 0 or self.knockback_y != 0:
            kbx = self.knockback_x * dt * 10
            kby = self.knockback_y * dt * 10
            if self.is_walkable(self.px + kbx, self.py): self.px += kbx
            if self.is_walkable(self.px, self.py + kby): self.py += kby
            self.knockback_x *= 0.9
            self.knockback_y *= 0.9

        for item in self.items[:]:
            if math.hypot(item[0]-self.px, item[1]-self.py) < 0.6:
                if item[2] == "health": self.hp = min(self.max_hp, self.hp+25)
                elif item[2] == "key": self.keys += 1
                elif item[2] == "score": self.score += 50
                elif item[2] == "weapon": self.ammo += 15
                self.items.remove(item)

        if self.hp <= 0: self.hp = 0; self.game_over = True
        mx, my = int(self.px), int(self.py)
        if 0 <= mx < MAP_W and 0 <= my < MAP_H and MAP[my][mx] == TILE_EXIT:
            self.victory = True

