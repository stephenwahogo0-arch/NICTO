# === ENEMY AI STATE MACHINE ===
AI_IDLE, AI_PATROL, AI_ALERT, AI_CHASE, AI_ATTACK, AI_FLEE, AI_SEARCH = range(7)

class EnemyAI:
    def __init__(self, x, y, hp=100):
        self.x = x; self.y = y; self.hp = hp; self.max_hp = hp
        self.state = AI_PATROL if random.random() < 0.7 else AI_IDLE
        self.speed = 1.2 + random.uniform(-0.3, 0.3)
        self.damage = 8 + random.randint(-2, 4)
        self.alert_timer = 0.0
        self.search_timer = 0.0
        self.attack_cooldown = 0.0
        self.patrol_target = (x, y)
        self.last_known_player = None
        self.detection_range = 8 + random.uniform(-1, 2)
        self.attack_range = 1.2

    def update(self, dt, px, py, walkable_fn):
        ddx = px - self.x
        ddy = py - self.y
        dist = math.hypot(ddx, ddy)
        self.alert_timer = max(0, self.alert_timer - dt)
        self.attack_cooldown = max(0, self.attack_cooldown - dt)
        self.search_timer = max(0, self.search_timer - dt)

        if self.state == AI_IDLE:
            if dist < self.detection_range:
                self.state = AI_ALERT
                self.alert_timer = 0.5
                self.last_known_player = (px, py)

        elif self.state == AI_PATROL:
            if dist < self.detection_range:
                self.state = AI_ALERT
                self.alert_timer = 0.5
                self.last_known_player = (px, py)
            else:
                tx, ty = self.patrol_target
                dx, dy = tx - self.x, ty - self.y
                pd = math.hypot(dx, dy)
                if pd < 0.5:
                    self.patrol_target = (random.randint(1, MAP_W-2), random.randint(1, MAP_H-2))
                elif pd > 0:
                    nx = self.x + (dx/pd) * self.speed * 0.4 * dt
                    ny = self.y + (dy/pd) * self.speed * 0.4 * dt
                    if walkable_fn(nx, self.y): self.x = nx
                    if walkable_fn(self.x, ny): self.y = ny

        elif self.state == AI_ALERT:
            self.alert_timer -= dt
            if self.alert_timer <= 0:
                self.state = AI_CHASE
                self.last_known_player = (px, py)

        elif self.state == AI_CHASE:
            if dist < self.attack_range:
                self.state = AI_ATTACK
            elif dist < self.detection_range * 2:
                if dist > 0:
                    nx = self.x + (ddx/dist) * self.speed * dt
                    ny = self.y + (ddy/dist) * self.speed * dt
                    if walkable_fn(nx, self.y): self.x = nx
                    if walkable_fn(self.x, ny): self.y = ny
                self.last_known_player = (px, py)
            else:
                self.state = AI_SEARCH
                self.search_timer = 3.0

        elif self.state == AI_ATTACK:
            if dist > self.attack_range * 1.5:
                self.state = AI_CHASE
            elif self.attack_cooldown <= 0:
                self.attack_cooldown = 1.0
                return "attack", self.damage
            if dist > 0:
                nx = self.x + (ddx/dist) * self.speed * 0.3 * dt
                ny = self.y + (ddy/dist) * self.speed * 0.3 * dt
                if walkable_fn(nx, self.y): self.x = nx
                if walkable_fn(self.x, ny): self.y = ny

        elif self.state == AI_FLEE:
            hp_ratio = self.hp / self.max_hp
            if hp_ratio > 0.3 and dist < self.detection_range:
                self.state = AI_CHASE
            elif dist > 0:
                nx = self.x + (-ddx/dist) * self.speed * 1.2 * dt
                ny = self.y + (-ddy/dist) * self.speed * 1.2 * dt
                if walkable_fn(nx, self.y): self.x = nx
                if walkable_fn(self.x, ny): self.y = ny

        elif self.state == AI_SEARCH:
            if self.last_known_player:
                lx, ly = self.last_known_player
                dx, dy = lx - self.x, ly - self.y
                pd = math.hypot(dx, dy)
                if pd < 1.0 or self.search_timer <= 0:
                    self.state = AI_PATROL
                    self.last_known_player = None
                elif pd > 0:
                    nx = self.x + (dx/pd) * self.speed * 0.6 * dt
                    ny = self.y + (dy/pd) * self.speed * 0.6 * dt
                    if walkable_fn(nx, self.y): self.x = nx
                    if walkable_fn(self.x, ny): self.y = ny

        if self.hp <= 0:
            return "death", 0
        return None, 0

# === PHYSICS ===
def point_in_rect(px, py, rx, ry, rw, rh):
    return rx <= px <= rx + rw and ry <= py <= ry + rh

def line_intersects_rect(x1, y1, x2, y2, rx, ry, rw, rh):
    steps = int(math.hypot(x2-x1, y2-y1) / 0.5) + 1
    for i in range(steps):
        t = i / max(steps, 1)
        px = x1 + (x2 - x1) * t
        py = y1 + (y2 - y1) * t
        if point_in_rect(px, py, rx, ry, rw, rh):
            return True
    return False

class Projectile:
    def __init__(self, x, y, angle, speed=6.0, damage=25):
        self.x = x; self.y = y
        self.dx = math.cos(angle) * speed
        self.dy = math.sin(angle) * speed
        self.damage = damage
        self.life = 2.0

    def update(self, dt, walkable_fn):
        self.x += self.dx * dt
        self.y += self.dy * dt
        self.life -= dt
        if not walkable_fn(self.x, self.y):
            return "hit_wall"
        return None

