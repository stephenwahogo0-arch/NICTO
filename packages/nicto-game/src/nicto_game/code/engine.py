class Player:
    def __init__(self, x, y):
        self.x, self.y = float(x), float(y)
        self.vx, self.vy = 0.0, 0.0
        self.w, self.h = 0.6, 0.8
        self.on_ground = False
        self.hp = PLAYER_HEALTH
        self.max_hp = PLAYER_HEALTH
        self.score = 0
        self.facing = 1

    def update(self, dt, keys, map_data):
        accel = PLAYER_SPEED * 8.0
        friction = 5.0
        if keys[K_a] or keys[K_LEFT]:
            self.vx -= accel * dt; self.facing = -1
        if keys[K_d] or keys[K_RIGHT]:
            self.vx += accel * dt; self.facing = 1
        if not (keys[K_a] or keys[K_LEFT] or keys[K_d] or keys[K_RIGHT]):
            self.vx *= max(0, 1 - friction * dt)
        self.vx = max(-PLAYER_SPEED*2, min(PLAYER_SPEED*2, self.vx))

        if (keys[K_w] or keys[K_UP] or keys[K_SPACE]) and self.on_ground:
            self.vy = PLAYER_JUMP
            self.on_ground = False

        self.vy += GRAVITY * dt
        self.vy = min(20.0, self.vy)

        new_x = self.x + self.vx * dt
        new_y = self.y + self.vy * dt

        self.on_ground = False
        if not self._collides(new_x, self.y, map_data):
            self.x = new_x
        else:
            self.vx = 0
        if not self._collides(self.x, new_y, map_data):
            self.y = new_y
        else:
            if self.vy > 0: self.on_ground = True
            self.vy = 0

    def _collides(self, x, y, map_data):
        for yy in range(int(y), int(y+self.h)+1):
            for xx in range(int(x), int(x+self.w)+1):
                if 0 <= xx < MAP_W and 0 <= yy < MAP_H:
                    if map_data[yy][xx] not in WALKABLE:
                        return True
                elif yy < 0: return True
        return False

